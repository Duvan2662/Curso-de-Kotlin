fun main () {
    
    //Aumento de variables

    println("----------------------AUMENTO DE VARIABLES-------------------")
    var count : Int = 10
    println("Valor cuando inicia de la variable: $count")
    count = count + 1
    println("Valor cuando incrementa en uno la variable: $count")
    
    //Otra manera de incrementar el valor de una variable
    var count2 : Int = 26
    println ("Valor inicial de la segunda variable $count2")
    count2++
    println ("Valor cuando incrementa la segunda variable: $count2")



    //Dismunucion de variables
 
    println("\n-------------------------DECREMENTO DE VARIABLES------------------------------------")
    var count3 : Int = 66
    println("Valor inicial de la tercera variable: $count3")
    count3--
    println("Valor cuando decrementa la tercera variable: $count3")
}